package com.yanxi.designmode.restore;

public class RestoreMemoryTo {
	private MemoryTo memoryTo;

	public MemoryTo getMemoryTo() {
		return memoryTo;
	}

	public void setMemoryTo(MemoryTo memoryTo) {
		this.memoryTo = memoryTo;
	}

	public RestoreMemoryTo(MemoryTo memoryTo) {
		super();
		this.memoryTo = memoryTo;
	}
	
}
